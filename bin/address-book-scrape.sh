#!/bin/sh
sqlite3 ~/Library/Application\ Support/AddressBook_hide/Sources/18A7BB2D-362B-407B-AB3F-22051F93A909/AddressBook-v22.abcddb <<'END_SQL'
.headers on
.mode csv
.output Desktop/Contacts/contact-dump.csv 
SELECT
  ZABCDRECORD.Z_PK,
  ZABCDRECORD.ZFIRSTNAME,
  ZABCDRECORD.ZMIDDLENAME,
  ZABCDRECORD.ZLASTNAME,
  ZABCDRECORD.ZNICKNAME,
  ZABCDRECORD.ZJOBTITLE,
  ZABCDRECORD.ZORGANIZATION,
  ZABCDPHONENUMBER.ZFULLNUMBER,
  ZABCDPHONENUMBER.ZLABEL,
  ZABCDEMAILADDRESS.ZADDRESS,
  ZABCDPOSTALADDRESS.ZLABEL,
  ZABCDPOSTALADDRESS.ZCITY,
  ZABCDPOSTALADDRESS.ZSTATE,
  ZABCDPOSTALADDRESS.ZZIPCODE,
  ZABCDPOSTALADDRESS.ZCOUNTRYNAME,
  ZABCDURLADDRESS.ZURL,
  ZABCDRECORD.ZBIRTHDAYYEAR

FROM ZABCDRECORD
  LEFT JOIN ZABCDPOSTALADDRESS ON ZABCDPOSTALADDRESS.ZOWNER = ZABCDRECORD.Z_PK
  LEFT JOIN ZABCDEMAILADDRESS ON ZABCDEMAILADDRESS.ZOWNER = ZABCDRECORD.Z_PK
  LEFT JOIN ZABCDPHONENUMBER ON ZABCDPHONENUMBER.ZOWNER = ZABCDRECORD.Z_PK
  LEFT JOIN ZABCDURLADDRESS ON ZABCDURLADDRESS.ZOWNER = ZABCDRECORD.Z_PK; 
  
.output stdout
END_SQL